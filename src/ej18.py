nombre = input("Dime tu nombre: ")
apellido1 = input("Dime tu primer apellido: ")
apellido2 = input("Dime tu segundo apellido: ")

nombre = nombre.lower()
apellido1 = apellido1.lower()
apellido2 = apellido2.lower()
print("Tu nombre en minusculas es: ",nombre,apellido1,apellido2)

nombre = nombre.upper()
apellido1 = apellido1.upper()
apellido2 = apellido2.upper()
print("Tu nombre en mayusculas es: ",nombre,apellido1,apellido2)

nombre = nombre.title()
apellido1 = apellido1.title()
apellido2 = apellido2.title()
print("La primera letra de tu nombre y apellidos en mayusculas es: ",nombre,apellido1,apellido2)